<?php

/* Operadores em incremento e decremento

    Servem para somar ou subtrair em (+1 ou -1) qualquer valor do tipo numérico ou string.

*/


$n1 = 20;
$n2 = 5;
$n3 = 20;

// Pré-incremento
echo ++$n1;
echo'<hr>';

// Pós-incremento
echo $n2++;
echo '<br>';
echo $n2;
echo '<hr>';

// Pré-decremento
echo --$n3;
echo'<hr>';

// Pós-decremento
echo $n3--;
echo'<br>';
echo $n3;
